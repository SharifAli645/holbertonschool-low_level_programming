#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - function that sum two numbers
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 **/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that rest two numbers
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 **/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that mul two numbers
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 **/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that div two numbers
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 **/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that % two numbers
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
